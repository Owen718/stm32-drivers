
#include "main.h"
/*************************************************************************
函数名称: void BEEP_Init(void) 
函数说明:蜂鸣器初始化程序
函数功能:
函数变量:
注    意:
*************************************************************************/
//初始化PD8为输出口.并使能这个口的时钟		    
//蜂鸣器初始化
void BEEP_Init(void)
{
	RCC->APB2ENR|=1<<5;    	//使能PORTD时钟	   	  
	GPIOD->CRH&=0XFFFFFFF0; 
	GPIOD->CRH|=0X00000003;	//PD.8 推挽输出    
	BEEP=0;					//关闭蜂鸣器输出
}






