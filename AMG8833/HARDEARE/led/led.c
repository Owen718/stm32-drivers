//初始化PB5和PE5为输出口.并使能这两个口的时钟		    
//LED IO初始化
void LED_Init(void)
{
	RCC->APB2ENR|=1<<2;    //使能PORTA时钟	   	 	
	   	 
	GPIOA->CRH&=0XF0FfFFFF; 
	GPIOA->CRH|=0X03000000;//PA.14 推挽输出   	 
    GPIOA->ODR|=1<<14;      //PA.14 输出高 
}






